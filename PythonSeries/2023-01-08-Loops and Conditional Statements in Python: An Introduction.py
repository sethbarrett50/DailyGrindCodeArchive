# Lists are an essential data structure in Python, allowing you to store and organize data in an ordered sequence. 
# You can create a list by enclosing a comma-separated sequence of values in square brackets.
# For example:
fruits = ['apple', 'banana', 'mango'] 

# A for loop is used to iterate over a sequence, such as a list or a string. 
# For example:
fruits = ['apple', 'banana', 'mango']
for fruit in fruits:
    print(fruit)

# A while loop, on the other hand, will execute a block of code as long as a certain condition is True. 
# For example:
i = 1
while i < 6:
    print(i)
    i += 1 

# Conditional statements, also known as if statements, allow you to execute a block of code only if a certain condition is met. 
# For example:
x = 10
if x > 15:
    print("x is greater than 15")
elif x > 5:
    print("x is greater than 5 but less than or equal to 15")
else:
    print("x is less than or equal to 5") 
