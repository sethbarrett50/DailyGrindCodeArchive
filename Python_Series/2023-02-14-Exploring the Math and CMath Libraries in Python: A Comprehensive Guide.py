# Python provides several built-in libraries for mathematical operations, including the math and cmath libraries.
# Here's an example of how to use the math library to calculate the square root of a number:
import math

# calculate the square root of 9
result = math.sqrt(9)
print(result)

# The cmath library is similar to the math library, but it is specifically designed for complex numbers.
# Here's an example of how to use the cmath library to calculate the square root of -9:
import cmath

# calculate the square root of -9
result = cmath.sqrt(-9)
print(result)


# In addition to these common mathematical functions, the math and cmath libraries also provide other useful functions such as the ability to calculate the factorial of a number, the greatest common denominator, and the least common multiple.
import math

#calculating factorial
result = math.factorial(5)
print(result)

import math

#calculating GCD
result = math.gcd(20, 8)
print(result)