# You can use modules in your code by using the import keyword, followed by the module name. 
# For example:
import math

print(math.pi)  
# Output: 3.141592653589793
print(math.cos(math.pi))  
# Output: -1.0

# You can also import specific functions or variables from a module using the from keyword. 
# For example:
from math import pi, cos

print(pi)  
# Output: 3.141592653589793
print(cos(pi))  
# Output: -1.0 

